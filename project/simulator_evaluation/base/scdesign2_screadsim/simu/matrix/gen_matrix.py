# gen_matrix.py


import sys, os
import scReadSim.Utility as Utility
import scReadSim.GenerateSyntheticCount as GenerateSyntheticCount
import scReadSim.scRNA_GenerateBAM as scRNA_GenerateBAM


root_dir = "/groups/cgsd/xianjie/projects/cna-benchmark/HCC3N_600spot/simulator_evaluation/base/gen_data/scdesign2_screadsim"
out_dir = os.path.join(root_dir, "simu/matrix")
sample_id = 'HCC3N_simu'
ncores = 10


INPUT_cells_barcode_file = os.path.join(root_dir, 'data/barcodes.tsv')
INPUT_bamfile = os.path.join(root_dir, 'simu/pp/processed.bam')
INPUT_genome_size_file = os.path.join(root_dir, 'data/hg38_chrom_sizes.tsv')
INPUT_genome_annotation = os.path.join(root_dir, "data/genes.gtf")


# Generate features
outdirectory = out_dir
os.makedirs(outdirectory, exist_ok = True)


# Specify the path to bed files generated by Utility.scRNA_CreateFeatureSets
gene_bedfile = os.path.join(outdirectory, "scReadSim.Gene.chr1-22.bed")
intergene_bedfile = os.path.join(outdirectory, "scReadSim.InterGene.chr1-22.bed")

# Specify the output count matrices' prenames
filename = sample_id
UMI_gene_count_mat_filename = "%s.gene.countmatrix" % filename
UMI_intergene_count_mat_filename = "%s.intergene.countmatrix" % filename

# Construct count matrix for genes
Utility.scRNA_bam2countmat_paral(
    cells_barcode_file = INPUT_cells_barcode_file,
    bed_file = gene_bedfile, 
    INPUT_bamfile = INPUT_bamfile, 
    outdirectory = outdirectory, 
    count_mat_filename = UMI_gene_count_mat_filename, 
    UMI_modeling = True, 
    UMI_tag = "UB:Z", 
    n_cores = ncores
)

# Construct count matrix for inter-genes
Utility.scRNA_bam2countmat_paral(
    cells_barcode_file = INPUT_cells_barcode_file, 
    bed_file = intergene_bedfile, 
    INPUT_bamfile = INPUT_bamfile, 
    outdirectory = outdirectory, 
    count_mat_filename = UMI_intergene_count_mat_filename, 
    UMI_modeling = True, 
    UMI_tag = "UB:Z", 
    n_cores = ncores
)

print("[bam2countmat] All Done!")
