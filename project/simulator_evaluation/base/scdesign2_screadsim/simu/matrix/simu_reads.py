# simu_reads.py

import pandas as pd
import sys, os
import scReadSim.Utility as Utility
import scReadSim.GenerateSyntheticCount as GenerateSyntheticCount
import scReadSim.scRNA_GenerateBAM as scRNA_GenerateBAM


root_dir = "/groups/cgsd/xianjie/projects/cna-benchmark/HCC3N_600spot/simulator_evaluation/base/gen_data/scdesign2_screadsim"
out_dir = os.path.join(root_dir, "simu")
sample_id = 'HCC3N_simu'
ncores = 10

INPUT_cells_barcode_file = os.path.join(root_dir, 'data/barcodes.tsv')
INPUT_bamfile = os.path.join(root_dir, 'simu/pp/processed.bam')
INPUT_genome_size_file = os.path.join(root_dir, 'data/hg38_chrom_sizes.tsv')
INPUT_genome_annotation = os.path.join(root_dir, "data/genes.gtf")

samtools_directory = "/home/xianjie/.anaconda3/envs/SCSC/bin"
macs3_directory = "/home/xianjie/.anaconda3/envs/SCSC/bin"
bedtools_directory = "/home/xianjie/.anaconda3/envs/SCSC/bin"
seqtk_directory = "/home/xianjie/.anaconda3/envs/SCSC/bin"
fgbio_jarfile = "/home/xianjie/.anaconda3/envs/SCSC/share/fgbio/fgbio.jar"
bowtie2_directory = "/home/xianjie/.anaconda3/envs/SCSC/bin"

referenceGenome_name = "genome"
referenceGenome_dir = os.path.join(root_dir, 'data/refgenome')
referenceGenome_file = os.path.join(referenceGenome_dir, "%s.fa" % referenceGenome_name)

features_dir = os.path.join(out_dir, 'features')
matrix_dir = os.path.join(out_dir, 'matrix')

# Specify the path to bed files generated by Utility.scRNA_CreateFeatureSets
gene_bedfile = os.path.join(features_dir, "scReadSim.Gene.chr1-22.bed")
intergene_bedfile = os.path.join(features_dir, "scReadSim.InterGene.chr1-22.bed")

# Specify the output count matrices' prenames
filename = sample_id
UMI_gene_count_mat_filename = "%s.gene.countmatrix" % filename
UMI_intergene_count_mat_filename = "%s.intergene.countmatrix" % filename



# Output synthetic read
reads_dir = os.path.join(out_dir, 'reads')
os.makedirs(reads_dir, exist_ok = True)

# Specify the names of synthetic count matrices (generated by GenerateSyntheticCount.scRNA_GenerateSyntheticCount)
synthetic_countmat_gene_file = UMI_gene_count_mat_filename + ".scDesign2Simulated.cna.txt"
synthetic_countmat_intergene_file = UMI_intergene_count_mat_filename + ".scDesign2Simulated.cna.txt"

# Specify the base name of bed files containing synthetic reads
OUTPUT_cells_barcode_file = "synthetic_cell_barcode.txt"
gene_read_bedfile_prename = "%s.syntheticBAM.gene" % filename
intergene_read_bedfile_prename = "%s.syntheticBAM.intergene" % filename
BED_filename_combined_pre = "%s.syntheticBAM.combined" % filename
synthetic_cell_label_file = UMI_gene_count_mat_filename + ".scDesign2Simulated.cna.CellTypeLabel.txt"


# Create synthetic read coordinates for genes
scRNA_GenerateBAM.scRNA_GenerateBAMCoord(
    bed_file = gene_bedfile, 
    UMI_count_mat_file = os.path.join(matrix_dir, synthetic_countmat_gene_file), 
    synthetic_cell_label_file = os.path.join(matrix_dir, synthetic_cell_label_file),
    read_bedfile_prename = gene_read_bedfile_prename, 
    INPUT_bamfile = INPUT_bamfile, 
    outdirectory = reads_dir, 
    OUTPUT_cells_barcode_file = OUTPUT_cells_barcode_file, 
    jitter_size = 5, 
    read_len = 98
)

# Create synthetic read coordinates for intergenes
scRNA_GenerateBAM.scRNA_GenerateBAMCoord(
    bed_file = intergene_bedfile, 
    UMI_count_mat_file = os.path.join(matrix_dir, synthetic_countmat_intergene_file), 
    synthetic_cell_label_file = os.path.join(matrix_dir, synthetic_cell_label_file), 
    read_bedfile_prename = intergene_read_bedfile_prename, 
    INPUT_bamfile = INPUT_bamfile, 
    outdirectory = reads_dir, 
    OUTPUT_cells_barcode_file = OUTPUT_cells_barcode_file, 
    jitter_size = 5, 
    read_len = 98
)

# Combine bed files
scRNA_GenerateBAM.scRNA_CombineBED(
    outdirectory = reads_dir, 
    gene_read_bedfile_prename = gene_read_bedfile_prename,
    intergene_read_bedfile_prename = intergene_read_bedfile_prename, 
    BED_filename_combined_pre = BED_filename_combined_pre
)

print("[CombineBED] All Done!")




# Convert BED files to FASTQ files
#referenceGenome_name = "chr1"
#referenceGenome_dir = "/home/users/example/refgenome_dir"  # may use users' own path
synthetic_fastq_prename = BED_filename_combined_pre


# Convert combined bed file into FASTQ files
scRNA_GenerateBAM.scRNA_BED2FASTQ(
    bedtools_directory = bedtools_directory, 
    seqtk_directory = seqtk_directory, 
    referenceGenome_file = referenceGenome_file, 
    outdirectory = reads_dir, 
    BED_filename_combined = BED_filename_combined_pre, 
    synthetic_fastq_prename = synthetic_fastq_prename
)

print("[BED2FASTQ] All Done!")





# Generate reads with errors in FASTQs
scRNA_GenerateBAM.scRNA_ErrorBase(
    fgbio_jarfile = fgbio_jarfile, 
    INPUT_bamfile = INPUT_bamfile, 
    referenceGenome_file = referenceGenome_file, 
    outdirectory = reads_dir, 
    synthetic_fastq_prename = synthetic_fastq_prename
)

print("[ErrorBase] All Done!")




# Align synthetic reads
output_BAM_pre = "%s.syntheticBAM.CBincluded" % filename

# Synthetic reads alignment
scRNA_GenerateBAM.AlignSyntheticBam_Single(
    bowtie2_directory = bowtie2_directory, 
    samtools_directory = samtools_directory, 
    outdirectory = reads_dir, 
    referenceGenome_name = referenceGenome_name, 
    referenceGenome_dir = referenceGenome_dir, 
    synthetic_fastq_prename = synthetic_fastq_prename, 
    output_BAM_pre = output_BAM_pre
)

# Synthetic reads (with sequencing errors) alignment
scRNA_GenerateBAM.AlignSyntheticBam_Single(
    bowtie2_directory = bowtie2_directory, 
    samtools_directory = samtools_directory, 
    outdirectory = reads_dir, 
    referenceGenome_name = referenceGenome_name, 
    referenceGenome_dir = referenceGenome_dir, 
    synthetic_fastq_prename = synthetic_fastq_prename + ".ErrorIncluded" , 
    output_BAM_pre = output_BAM_pre + ".ErrorIncluded"
)
